package vts.snystems.sns.vts.pojo;

public class DistanceSummary
{
    private String vImei;
    private String vNumber;
    private String vType;
    private String vColor;
    private String vStatus;
    private String vDistance;
    private String vTargetName;

    public String getvImei() {
        return vImei;
    }

    public void setvImei(String vImei) {
        this.vImei = vImei;
    }

    public String getvNumber() {
        return vNumber;
    }

    public void setvNumber(String vNumber) {
        this.vNumber = vNumber;
    }

    public String getvType() {
        return vType;
    }

    public void setvType(String vType) {
        this.vType = vType;
    }

    public String getvColor() {
        return vColor;
    }

    public void setvColor(String vColor) {
        this.vColor = vColor;
    }

    public String getvDistance() {
        return vDistance;
    }

    public void setvDistance(String vDistance) {
        this.vDistance = vDistance;
    }

    public String getvTargetName() {
        return vTargetName;
    }

    public void setvTargetName(String vTargetName) {
        this.vTargetName = vTargetName;
    }

    public String getvStatus() {
        return vStatus;
    }

    public void setvStatus(String vStatus) {
        this.vStatus = vStatus;
    }
}
