package vts.snystems.sns.vts.pojo;

public class TrackingInfo
{
    private String vImei;
    private String vType;
    private String vColor;
    private String vNumber;
    private String vlat;
    private String vlng;
    private String vLastTrackTime;
    private String vStatus;
    private String vTargetName;
    private String vAddress;

    public String getvImei() {
        return vImei;
    }

    public void setvImei(String vImei) {
        this.vImei = vImei;
    }

    public String getvType() {
        return vType;
    }

    public void setvType(String vType) {
        this.vType = vType;
    }

    public String getvColor() {
        return vColor;
    }

    public void setvColor(String vColor) {
        this.vColor = vColor;
    }

    public String getvNumber() {
        return vNumber;
    }

    public void setvNumber(String vNumber) {
        this.vNumber = vNumber;
    }

    public String getVlat() {
        return vlat;
    }

    public void setVlat(String vlat) {
        this.vlat = vlat;
    }

    public String getVlng() {
        return vlng;
    }

    public void setVlng(String vlng) {
        this.vlng = vlng;
    }

    public String getvLastTrackTime() {
        return vLastTrackTime;
    }

    public void setvLastTrackTime(String vLastTrackTime) {
        this.vLastTrackTime = vLastTrackTime;
    }

    public String getvStatus() {
        return vStatus;
    }

    public void setvStatus(String vStatus) {
        this.vStatus = vStatus;
    }

    public String getvTargetName() {
        return vTargetName;
    }

    public void setvTargetName(String vTargetName) {
        this.vTargetName = vTargetName;
    }

    public String getvAddress() {
        return vAddress;
    }

    public void setvAddress(String vAddress) {
        this.vAddress = vAddress;
    }
}
