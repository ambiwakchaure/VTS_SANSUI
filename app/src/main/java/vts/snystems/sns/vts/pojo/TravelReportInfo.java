package vts.snystems.sns.vts.pojo;

public class TravelReportInfo
{
    private String deviceName;
    private String deviceNumber;
    private String runningTime;
    private String idleTime;
    private String stopTime;
    private String inactiveTime;
    private String alertsName;
    private String kmDetails;
    private String avgSpeed;
    private String maxSpeed;

    public String getDeviceName() {
        return deviceName;
    }

    public void setDeviceName(String deviceName) {
        this.deviceName = deviceName;
    }

    public String getDeviceNumber() {
        return deviceNumber;
    }

    public void setDeviceNumber(String deviceNumber) {
        this.deviceNumber = deviceNumber;
    }

    public String getRunningTime() {
        return runningTime;
    }

    public void setRunningTime(String runningTime) {
        this.runningTime = runningTime;
    }

    public String getIdleTime() {
        return idleTime;
    }

    public void setIdleTime(String idleTime) {
        this.idleTime = idleTime;
    }

    public String getStopTime() {
        return stopTime;
    }

    public void setStopTime(String stopTime) {
        this.stopTime = stopTime;
    }

    public String getInactiveTime() {
        return inactiveTime;
    }

    public void setInactiveTime(String inactiveTime) {
        this.inactiveTime = inactiveTime;
    }

    public String getAlertsName() {
        return alertsName;
    }

    public void setAlertsName(String alertsName) {
        this.alertsName = alertsName;
    }

    public String getKmDetails() {
        return kmDetails;
    }

    public void setKmDetails(String kmDetails) {
        this.kmDetails = kmDetails;
    }

    public String getAvgSpeed() {
        return avgSpeed;
    }

    public void setAvgSpeed(String avgSpeed) {
        this.avgSpeed = avgSpeed;
    }

    public String getMaxSpeed() {
        return maxSpeed;
    }

    public void setMaxSpeed(String maxSpeed) {
        this.maxSpeed = maxSpeed;
    }
}
